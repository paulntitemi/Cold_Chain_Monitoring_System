/**
 * Deterministic mock data for the ColdTrack Control Centre (London deployment).
 * Timestamps are computed relative to "now" on import so everything stays fresh
 * on page reload. Real API responses will drop into the same shapes.
 *
 * Phone numbers use Ofcom's reserved 07700 900xxx range, which is set aside
 * for drama/fiction and will never reach a real subscriber.
 */

const now = Date.now();
const minutes = (m: number) => new Date(now - m * 60_000).toISOString();
const hours = (h: number) => new Date(now - h * 3_600_000).toISOString();
const days = (d: number) => new Date(now - d * 86_400_000).toISOString();
const future = (m: number) => new Date(now + m * 60_000).toISOString();
const futureDays = (d: number) => new Date(now + d * 86_400_000).toISOString();

function seriesInRange(count: number, base = 4.2, drift = 0.4): TemperatureReading[] {
  const out: TemperatureReading[] = [];
  for (let i = count - 1; i >= 0; i--) {
    const noise = Math.sin(i * 0.8) * drift + (Math.random() - 0.5) * 0.2;
    out.push({
      timestamp: minutes(i * 2),
      temperature: +(base + noise).toFixed(2),
      humidity: 40 + Math.round(Math.random() * 15),
    });
  }
  return out;
}

function seriesExcursion(
  count: number,
  breachAfter: number,
  peak = 10.4,
): TemperatureReading[] {
  const out: TemperatureReading[] = [];
  for (let i = count - 1; i >= 0; i--) {
    const idx = count - 1 - i;
    let temp: number;
    if (idx < breachAfter) {
      temp = 5.0 + Math.sin(idx * 0.6) * 0.6 + (Math.random() - 0.5) * 0.2;
    } else {
      const climb = (idx - breachAfter) / Math.max(1, count - breachAfter);
      temp = 5.0 + climb * (peak - 5.0) + (Math.random() - 0.5) * 0.25;
    }
    out.push({
      timestamp: minutes(i * 2),
      temperature: +temp.toFixed(2),
      humidity: 40 + Math.round(Math.random() * 15),
    });
  }
  return out;
}

