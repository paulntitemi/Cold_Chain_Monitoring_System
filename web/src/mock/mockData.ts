// ---------- Custody events helper ----------

function custody(
  batchId: string,
  events: Array<Partial<CustodyEvent> & { timestamp: string; eventType: CustodyEvent['eventType']; location: string; handledBy: string }>,
): CustodyEvent[] {
  return events.map((e, i) => ({
    id: `${batchId}-CUST-${i + 1}`,
    notes: e.notes,
    tempAtEvent: e.tempAtEvent,
    ...e,
  }));
}

